SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED;
SET NOCOUNT ON;
SET ANSI_NULLS ON;
SET QUOTED_IDENTIFIER ON;
USE AP;

SELECT
	TOP 5
	VendorID,
	InvoiceTotal
FROM
	Invoices
ORDER BY InvoiceTotal DESC;

SELECT
	TOP 5 PERCENT
	VendorID,
	InvoiceTotal
FROM
	Invoices
ORDER BY InvoiceTotal DESC;

SELECT
	TOP 5 WITH TIES
	VendorID,
	InvoiceDate
FROM
	Invoices
ORDER BY InvoiceTotal ASC;
